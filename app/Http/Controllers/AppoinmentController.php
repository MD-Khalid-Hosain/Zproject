    {
        Appoinment::create([
            'consultant_id' => $request->consultant_id,
            'name' => $request->name,
            'mobile' => $request->mobile,
            'email' => $request->email,
            'from' => $request->from,
            'to' => $request->to,
            'duration' => $request->duration,
            'status' => 0,
            'created_at' => Carbon::now(),

        ]);
        return back();
    }

